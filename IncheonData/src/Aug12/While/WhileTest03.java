package Aug12.While;

public class WhileTest03 {

	public static void main(String[] args) {
		/*
		 * 1번입니다. 2번입니다. 3번입니다. .args.
		 * 
		 * 10번입니다.
		 * 
		 * while문을 사용해서 해주세요.
		 */

		int count = 0;
		while (count < 10) {
			count++;
			System.out.printf("%d번 입니다. \n", count);
		}

	}
}
